function setLanguage(lang) {
    localStorage.setItem('tsekv_lang', lang);
    const t = translations[lang];
    if (!t) return;

    // Helper to safe update
    const setTxt = (id, key) => {
        const el = document.getElementById(id);
        if (el && t[key]) {
            // Check if HTML content needed
            if (key === 'about-p2' || key === 'finesse-btn' || key === 'hero-title' || key === 'hero-desc' || key === 'concept-text' || key === 'teaser-desc') {
                el.innerHTML = t[key];
            } else {
                el.innerText = t[key];
            }
        }
    };

    // Nav
    setTxt('nav-about', 'nav-about');
    setTxt('nav-collection', 'nav-collection');
    setTxt('nav-contact', 'nav-contact');

    // Index
    setTxt('hero-title', 'hero-title');
    setTxt('hero-desc', 'hero-desc');
    setTxt('concept-title', 'concept-title');
    setTxt('concept-text', 'concept-text');
    setTxt('btn-read-more', 'btn-read-more');
    setTxt('section-latest', 'section-latest');

    setTxt('card-1-desc', 'card-1-desc');
    setTxt('card-2-desc', 'card-2-desc');
    setTxt('card-3-desc', 'card-3-desc');

    setTxt('btn-view-collection', 'btn-view-collection');
    setTxt('teaser-title', 'teaser-title');
    setTxt('teaser-desc', 'teaser-desc');
    setTxt('btn-launch', 'btn-launch');

    // About
    setTxt('about-title', 'about-title');
    setTxt('about-p1', 'about-p1');
    setTxt('about-p2', 'about-p2');
    setTxt('about-p3', 'about-p3');

    // Contact
    setTxt('contact-title', 'contact-title');
    setTxt('contact-sub', 'contact-sub');
    setTxt('lbl-name', 'lbl-name');
    setTxt('lbl-email', 'lbl-email');
    setTxt('lbl-msg', 'lbl-msg');
    setTxt('btn-submit', 'btn-submit');
    setTxt('info-phone', 'info-phone');
    setTxt('info-email', 'info-email');
    setTxt('info-loc', 'info-loc');

    // Finesse (Dynamic elements handled by finesse.html logic separately or synced here)
    setTxt('rec-title', 'rec-title');
    // Note: Finesse weather text is updated dynamically by JS based on weather, 
    // we need to expose currentLang for finesse.html to use.
    window.currentLang = lang;

    // Collection Sidebar
    setTxt('filter-new', 'filter-new');
    setTxt('filter-all', 'filter-all');
    setTxt('filter-fav', 'filter-fav');
    setTxt('filter-arrival', 'filter-arrival');
    setTxt('cat-header', 'cat-header');
    setTxt('cat-coat', 'cat-coat');
    setTxt('cat-jacket', 'cat-jacket');
    setTxt('cat-dress', 'cat-dress');
    setTxt('cat-knit', 'cat-knit');
    setTxt('cat-shirt', 'cat-shirt');
    setTxt('cat-trouser', 'cat-trouser');
    setTxt('cat-jeans', 'cat-jeans');
    setTxt('cat-skirt', 'cat-skirt');
    setTxt('cat-shoes', 'cat-shoes');

    // Update Active Buttons
    document.querySelectorAll('.lang-switcher button').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('onclick').includes(lang));
    });

    // Dispatch event for other scripts to react
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

// Init on load
document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('tsekv_lang') || 'jp';
    setLanguage(saved);
});
